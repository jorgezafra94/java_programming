package Primitives;

public class BooleanPrimitive {
    /*
    * estos son valore logico o true o false, se utilizan para flujos de control y toma de decisiones a partir de comparaciones
    */

    boolean x = true;
    boolean y = false;
    boolean z = 23 > 12;

    /*
    * aqui se pueden usar:
    * == (igualdad de objeto o para primitivos es valor)
    * != (diferente objeto o para primitivos es valor)
    * > (mayor)
    * < (menor)
    * >= (mayor o igual)
    * <= (menor o igual)
    * .equals (es un metodo que tienen todos los objetos y este evalua valor mas no que tengan la misma direccion de memmoria)
    */
}
